function make_map_codepoint_to_notations(records) {
  const map = new Map();
  for (const record of records) {
    let notations = map.get(record[0]);
    if (notations === undefined) {
      map.set(record[0], [record[2]]);
    } else {
      map.set(record[0], [...notations, record[2]]);
    }
  }
  return map;
}

